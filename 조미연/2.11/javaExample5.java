class  javaExample5
{
	public static void main(String[] args) 
	{
		// 문자열 배열 만들기
		String[][] text = new String[9][9];
		//실행결과
		// 1단을 출력합니다.
		// text[0][0] : 1*1=1 ...

		//스트링 인트로 변환

		// 배열에 값 대입.
		// 첫번째 배열에 대입
		for(int i = 1; i < 10; i++){
			text[i][1] = 1 * (i+1);		
		}// for문 >  i

			System.out.println(text[i][1]);

		// 두번째 배열에 대입



	}//main
} //class
